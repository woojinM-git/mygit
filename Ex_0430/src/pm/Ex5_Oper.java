package pm;

public class Ex5_Oper {

	public static void main(String[] args) {
		// 프로그램 시작
		
		int v1 = 7;
		int v2 = 10;
		
		boolean res = v1 > v2; // v1이 v2보다 크다면 true
		System.out.println("v1 > v2 = " + res); // v1 > v2 = false

		res = v1 >= v2; // v1이 v2보다 크거나 같다면 true
		System.out.println("v1 > v2 = " + res); // v1 > v2 = false
	}

}
